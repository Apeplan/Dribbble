    /**
     * 解析 UTC 时间
     *
     * @param sTime 2016-01-05T15:09:54Z
     * @return
     */
    public static String formatUTC(String sTime) {
        //T代表后面跟着时间，Z代表UTC统一时间
        //解析时间 2016-01-05T15:09:54Z
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd'T'HH:mm:ss'Z'");
        Date date = toDate(sTime, sdf);
        String time = "";
        if (null != date) {
            time = formatDateTime(date, "yyyy-MM-dd HH:mm:ss");
        }
        return time;
    }

